package GeneralUses;

public class TrainglePrint {

	public static void main(String[] args) {
		int a=0;

		for (int i =0;i<=5;i++){
			for (int k=5;k>=i;k--){
				
				System.out.print(" ");
			}
			for (int j=0;j<=i;j++){
				
				a=a+1;
				System.out.print(a);
			}
			
			System.out.println(" ");
		} 
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
